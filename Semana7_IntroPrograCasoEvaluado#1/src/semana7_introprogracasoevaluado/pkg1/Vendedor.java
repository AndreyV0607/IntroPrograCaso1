public class Vendedor {
    //atributos
    private String nombreVendedor;
    private double cedulaVendedor;
    private String codigoVendedor;
    private String sucursal;
    private String vehículo;
    //constructor
    public Vendedor() {
    }

    

    
    //setters&getters
    public String getNombreVendedor() {
        return nombreVendedor;
    }

    public void setNombreVendedor(String nombreVendedor) {
        this.nombreVendedor = nombreVendedor;
    }

    public double getCedulaVendedor() {
        return cedulaVendedor;
    }

    public void setCedulaVendedor(double cedulaVendedor) {
        this.cedulaVendedor = cedulaVendedor;
    }

    public String getCodigoVendedor() {
        return codigoVendedor;
    }

    public void setCodigoVendedor(String codigoVendedor) {
        this.codigoVendedor = codigoVendedor;
    }

    public String getSucursal() {
        return sucursal;
    }

    public void setSucursal(String sucursal) {
        this.sucursal = sucursal;
    }

    public String getVehículo() {
        return vehículo;
    }

    public void setVehículo(String vehículo) {
        this.vehículo = vehículo;
    }
    
    
    
}
